# Some benefits of OOP in Ruby:
# - Organize code into logical groups with domain-specific names, which
#   simplifies program organization while improving flow and conceptualization.
#   - That enables encapsulation, which has these benefits:
#     - Data protection that supports sepration of concerns.
#     - Abstraction that supports comprehension and code reusability.
# - Reuse code through inheritance (classes, Is-a/hierarchical relationship)
#   or mixins (modules, Has-a/non-hierarchical relationship).
